    private void jButton_add_itemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_add_itemActionPerformed
        Item item = new Item();
        while(true){
            String item_name = jTextField_item_name.getText();
            item.setItemName(item_name);
            
            String input_quantity = jTextField_quantity.getText();
            item.setQuantity(input_quantity);
            
            String input_stock_price = jTextField_stock_price.getText();
            item.setStockPrice(input_stock_price);
            
            if (item.getItemName() == null || item.getQuantity() ==0 || item.getStockPrice() == 0){
                break;
            }
            String item_details = item_name + "|" + input_quantity + "|" + input_stock_price;
            
            String file_path = "src\\main\\java\\com\\mycompany\\JavaY2\\TextFile\\inventory";
            try (BufferedWriter bw = new BufferedWriter(new FileWriter(file_path, true))){
                bw.write(item_details + "\n");
                JOptionPane.showMessageDialog(null, "Item added successfully");
                this.dispose();
                break;

            }catch (IOException e){
                JOptionPane.showMessageDialog(null, "Error writing to file: " + e.getMessage());
            }
        }    
    }//GEN-LAST:event_jButton_add_itemActionPerformed

