    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String keyword = jTextField1.getText().toLowerCase();
        if (keyword.isEmpty()) {
            UpdateTable.forUser(jTable1);
        }else{
            ObjectList objectList = new ObjectList();
            List<User> users = objectList.getUsers();
            String[][] matrix = new String[users.size()][4];
            User user;
            for (int i = 0; i < users.size(); i++) {
                user = users.get(i);
                if (user.anyMatch(keyword)) {
                    user = users.get(i);
                    matrix[i][0] = user.user_id;
                    matrix[i][1] = user.username;
                    matrix[i][2] = user.password;
                    matrix[i][3] = user.role;
                }
            }
            matrix = Matrix.removeEmptyRows(matrix);
            jTable1.setModel(new DefaultTableModel(
                                     matrix,
                                     new String [] {
                                             "User_ID", "Username", "Password","Role"
                                     }
                             ){
                                 @Override
                                 public boolean isCellEditable(int row, int column) {
                                     return false; // This makes all cells non-editable
                                 }
                             }
            );
        }
    }//GEN-LAST:event_jButton1ActionPerformed

