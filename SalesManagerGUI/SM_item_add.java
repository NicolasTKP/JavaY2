    private void populateSupplierComboBox(){
        try{
            BufferedReader br = new BufferedReader(new FileReader(supplier_file_path));
            String line;
            br.readLine(); // Skip header line

            while ((line = br.readLine()) != null) {
                String[] columns = line.split("\\|");
                if (columns.length >= 2) {
                    String supplier_name = columns[1].trim();
                    supplier_combo_box.addItem(supplier_name);
                }
            }
        }catch(IOException e){
            System.out.println("Error reading supplier textfile");
        }
        
    }
