    private void populateItemComboBox(){
        try{
            BufferedReader br = new BufferedReader(new FileReader(inventory_file_path));
            String line;
            br.readLine(); // Skip header line

            while ((line = br.readLine()) != null) {
                String[] columns = line.split("\\|");
                if (columns.length >= 2) {
                    String item_name = columns[1].trim();
                    item_combo_box.addItem(item_name);
                }
            }
        }catch(IOException e){
            System.out.println("Error reading inventory textfile");
        }
        
    }
    
    private void setCurrentDate(){
        Date current_date = new Date();
        SimpleDateFormat date_format = new SimpleDateFormat("ddMMyyyy");
        String formatted_date = date_format.format(current_date);
        date_textfield.setText(formatted_date);
    }
    
    private void setRetailPrice(){
        DataMapping mapping = new DataMapping();
        Map<String,String> item_retail_price_map = mapping.NameRetailPriceMapping(inventory_file_path);
        String selected_item = (String) item_combo_box.getSelectedItem();
        String retail_price = item_retail_price_map.get(selected_item);
        retail_price_textfield.setText(retail_price);
    } 
    
    private void calculateTotalAmount(){
        int quantity = (int) quantity_spinner.getValue();
        double retail_price = Double.parseDouble(retail_price_textfield.getText());
        double total_amount = quantity*retail_price;
        total_amount_textfield.setText(String.format("%.2f", total_amount));
    }
