    private void add_sales_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_add_sales_buttonActionPerformed
        DailySale sales = new DailySale();
        
        String daily_sales_id = sales.setSalesID();
        String selected_item = (String) item_combo_box.getSelectedItem();
        int quantity = (int) quantity_spinner.getValue();
        double retail_price = Double.parseDouble(retail_price_textfield.getText());
        String date = date_textfield.getText();
        
        DataMapping mapping = new DataMapping();
        Map<String, String> item_map = mapping.NameIdMapping(inventory_file_path);
        String group_id = item_map.get(selected_item);
        
        String sales_details = daily_sales_id + "|" + quantity + "|" + group_id + "|" + retail_price + "|" + date;
        

        if (quantity <=0){
            JOptionPane.showMessageDialog(this, "Please enter a valid item quantity.");
        }else{
            boolean update_inventory = DailySale.updateInventoryQuantity(this, group_id, quantity);
            if (update_inventory){
                TextFile.addLine(daily_sales_file_path, sales_details);
                JOptionPane.showMessageDialog(this, "New sales record has been added.");
                this.dispose();                   
            }
        }
    }//GEN-LAST:event_add_sales_buttonActionPerformed

