    private void add_inventory_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_add_inventory_buttonActionPerformed

        String inventory_file_path = "src\\main\\java\\com\\mycompany\\JavaY2\\TextFile\\inventory";
        
        String group_id = group_id_textfield.getText();
        String item_name = item_name_textfield.getText().toLowerCase();
        double retail_price = (double) retail_price_spinner.getValue();
        retail_price = Double.parseDouble(String.format("%.2f", retail_price));
        int quantity = (int) quantity_spinner.getValue();
        
        if (quantity < 0 || retail_price < 0) {
            JOptionPane.showMessageDialog(this, "Quantity and Retail Price must be greater than zero.");
        }else{
            String inventory_details = group_id + "|" + item_name + "|" + quantity + "|" + retail_price;
            TextFile.addLine(inventory_file_path, inventory_details);
            JOptionPane.showMessageDialog(this, "New item group has been added to the inventory.");
            this.dispose();              
        }
    }//GEN-LAST:event_add_inventory_buttonActionPerformed

