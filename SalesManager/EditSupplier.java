    private void jButton_edit_supplierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_edit_supplierActionPerformed
        String filePath = "src\\main\\java\\com\\mycompany\\JavaY2\\TextFile\\suppliers";
        Supplier supplier = new Supplier();

        String supplier_id = jTextField_supplier_id.getText();  
        
        String supplier_name = jTextField_supplier_name.getText();
        supplier.setSupplierName(supplier_name);
        
        String address = jTextField_address.getText();
        supplier.setAddress(address);        
        
        String contact_number = jTextField_contact_number.getText();
        supplier.setContact(contact_number);   
        
        String input_supply_items = jTextField_supply_item.getText();
        supplier.setSupplyItems(input_supply_items);
        
        String payment_term = jTextField_payment_term.getText();
        supplier.setPaymentTerm(payment_term);

        try {
            BufferedReader br = new BufferedReader(new FileReader(filePath));
            StringBuilder updatedContent = new StringBuilder();
            String line;
            boolean found = false;

            while ((line = br.readLine()) != null) {
                String[] columns = line.split("\\|");

                if (columns.length < 5) {
                    updatedContent.append(line).append("\n");  
                    continue;
                    }

                if (columns[0].trim().equals(supplier_id.trim())) {
                    line = supplier_id + "|" + supplier_name + "|" + address + "|" + contact_number + "|" + supplier.setSupplyItems(input_supply_items) + "|" + payment_term;
                    found = true;
                }
                updatedContent.append(line).append("\n");  
            }
            br.close();

            if (!found) {
                JOptionPane.showMessageDialog(this, "Supplier not found!", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            BufferedWriter bw = new BufferedWriter(new FileWriter(filePath, false));
            bw.write(updatedContent.toString());
            bw.close();

            JOptionPane.showMessageDialog(this, "Supplier updated successfully!");
            this.dispose();  // Close the edit window

       }catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Error updating supplier: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton_edit_supplierActionPerformed

